#Data Types

# String - always inside "": "123", "ewrsdf!@#"
print("Hello"[0])
print("Hello"[4])
# [] - string's symbol position number

# Integer - целое число: 123, 56, 43535, 1 и т.д. Записывается как есть - без ковычек.
print(1 + 2) != print("1" + "2")

# Float - числа с плавающей точкой (дроби)
print(3.324)

# Boolean - вариация истины: Правда, Ложь.
True != true
False != false
# Всегда с заглавной буквы!